package models;


public class IntestazioneRapportoCCIType {
	private ServiceInput serviceInput;
	private String codConto;
	private String codSportello;
	
	
	public ServiceInput getServiceInput() {
		return serviceInput;
	}
	public void setServiceInput(ServiceInput serviceInput) {
		this.serviceInput = serviceInput;
	}
	public String getCodConto() {
		return codConto;
	}
	public void setCodConto(String codConto) {
		this.codConto = codConto;
	}
	public String getCodSportello() {
		return codSportello;
	}
	public void setCodSportello(String codSportello) {
		this.codSportello = codSportello;
	}
	
	

}
